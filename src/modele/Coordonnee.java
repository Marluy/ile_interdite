package modele;

/**
 *
 * @author tosiv
 */
public class Coordonnee
{
    private int posX;
    private int posY;
    
    public Coordonnee(int x, int y)
    {
	posX = x;
	posY = y;
    }

    /**
     * @return the posX
     */
    public int getPosX() {
        return posX;
    }

    /**
     * @param posX the posX to set
     */
    public void setPosX(int posX) {
        this.posX = posX;
    }

    /**
     * @return the posY
     */
    public int getPosY() {
        return posY;
    }

    /**
     * @param posY the posY to set
     */
    public void setPosY(int posY) {
        this.posY = posY;
    }
}
